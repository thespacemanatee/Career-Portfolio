class Task {
  constructor(
    socCode,
    title,
    taskId,
    task,
    dwaId,
    dwaTitle,
    iwaId,
    iwaTitle,
    gwaId,
    gwa
  ) {
    this.socCode = socCode;
    this.title = title;
    this.taskId = taskId;
    this.task = task;
    this.dwaId = dwaId;
    this.dwaTitle = dwaTitle;
    this.iwaId = iwaId;
    this.iwaTitle = iwaTitle;
    this.gwaId = gwaId;
    this.gwa = gwa;
  }
}

export default Task;
